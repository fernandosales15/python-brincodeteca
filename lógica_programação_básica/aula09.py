#Operadores Aritmeticos

adicao = 10 + 10 #Soma os dois números
print("Resultado da Adição:", adicao)

subtracao = 722 - 22  #Subtra o segundo número do primeiro
print("Resultado da Subtração:", subtracao)

multiplicacao = 10 * 100 #Multiplica os dois números
print("Resultado da Multiplicação:", multiplicacao)

divisao = 1000 / 10 #Divide o primeiro número pelo segundo
print("Resultado da Divisão:", divisao)

divisao_inteira = 1000 // 7 #Divide o primeiro número pelo segundo e retorna a parte inteira
print("Resultado da Divisão Inteira:", divisao_inteira)

operador_modulo = 50 % 3 #Retorna o resto da divisão do primeiro número pelo segundo
print("Resultado do Módulo (Resto da Divisão):", operador_modulo)

exponenciacao = 2 ** 10 #Eleva o primeiro número à potência do segundo
print("Resultado da Exponenciação:", exponenciacao)
