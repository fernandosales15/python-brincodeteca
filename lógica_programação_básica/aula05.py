#Aula sobre 'booleanos' ou seja 'true' ou 'false'

print(10 == "Verdadeiro") #Imprime 'false' pois sáo tipos diferentes (int e str) 
print(10 == 10) #Imprime 'true' pois sáo do mesmo tipo (int) e mesmo valor 

print(type(10 == 10)) #Imprime o tipo do dado '10 == 10' que é 'bool' (booleano) (true)
print(type(10 == "Verdadeiro")) #Imprime o tipo do dado '10 == "Verdadeiro"' que é 'bool' (booleano) (false)  
