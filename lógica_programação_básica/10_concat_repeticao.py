# 1. Concatenação com sinal de +
nome_concatenado = "Fernando" + " " + "Sales"
print(nome_concatenado)

# 2. Repetição com sinal de *
letras_repetidas = "A" * 20
print(letras_repetidas)

repeticao_nome = "Fernando" * 5
print(repeticao_nome)

# 3. F-strings (Recurso moderno do Python para formatar textos)
# É mais limpo do que usar vários sinais de +
print(f"O resultado da concatenação é: {nome_concatenado}")